class Account {
    //Estos son atributos
    Integer id;
    String name;
    String document;
    String email;
    String password;
    
    //Constructor de Account
    public Account( Integer id, String name, String document, String email, String password){
        this.id = id;
        this.name = name;
        this.document = document;
        this.email = email;
        this.password = password;
    }

}